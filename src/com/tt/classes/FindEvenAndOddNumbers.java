package com.tt.classes;

public class FindEvenAndOddNumbers {

	public static void main(String[] args) {
		// enter a number
		int x = 80;

		if (x % 2 == 0) {
			System.out.println("This is a even number");
		} else {
			System.out.println("This is a odd number");
		}

	}// end of class

}// end of class
